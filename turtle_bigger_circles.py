import turtle as t
radius = eval(input("Enter the radius of your circle : "))

t.speed(5)
t.color("blue")
t.pensize(10)
t.penup()
t.goto(-150, -10)
t.pendown()
t.circle(radius)

t.penup()
t.setx(0)
t.pendown()
t.color("black")
t.circle(radius)

t.penup()
t.setx(150)
t.pendown()
t.color("red")
t.circle(radius)
t.penup()
t.goto(70, -radius)
t.pendown()
t.color("yellow")
t.circle(radius)

t.penup()
t.setx(-70)
t.pendown()
t.color("green")
t.circle(radius)

t.done()