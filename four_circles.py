import turtle
t = turtle

t.penup()
t.color("red")
t.goto(-100,20)
t.pendown()
t.circle(45)

t.penup()
t.color("blue")
t.goto(-9, 20)
t.pendown()
t.circle(45)

t.penup()
t.color("black")
t.goto(-100, -70)
t.pendown()
t.circle(45)

t.penup()
t.color("yellow")
t.goto(-9, -70)
t.pendown()
t.circle(45)

t.done()