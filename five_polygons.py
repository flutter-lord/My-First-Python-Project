import turtle as t

#t.speed(1)
t.penup()
t.goto(-200, -80)
t.pendown()

t.backward(100)
t.left(60)
t.begin_fill()
t.fillcolor("grey")
t.forward(100)
t.right(120)
t.forward(100)
t.end_fill()

t.penup()
t.setx(-50)
t.pendown()
t.setheading(180)
t.forward(100)
t.begin_fill()
t.fillcolor("black")
t.right(90)
t.forward(100)
t.right(90)
t.forward(100)
t.right(90)
t.forward(100)
t.end_fill()

t.penup()
t.setx(20)
t.pendown()
t.begin_fill()
t.fillcolor("azure")
t.left(55)
t.circle(60, steps= 5)
t.end_fill()

t.penup()
t.setx(250)
t.left(65)
t.pendown()
t.begin_fill()
t.fillcolor("silver")
t.circle(60, steps= 6)
t.end_fill()
t.hideturtle()

t.penup()
t.setx(400)
t.pendown()
t.begin_fill()
t.fillcolor("snow")
t.circle(60, steps= 7)
t.end_fill()
t.hideturtle()



t.done()